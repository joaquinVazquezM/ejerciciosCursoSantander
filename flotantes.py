# Definir dos números flotantes
numero1 = 3.14
numero2 = 2.5


# Mostrar los números
print("Números flotantes:")
print("Número 1:", numero1)
print("Número 2:", numero2)

# Realizar operaciones
print("\nOperaciones:")
print("Suma:", numero1 + numero2)
print("Resta:", numero1 - numero2)
print("Multiplicación:", numero1 * numero2)
print("División:", numero1 / numero2)

# Operaciones adicionales
print("\nOperaciones con flotantes:")
# Valor absoluto
print("Valor absoluto de un número negativo:", abs(-5.7))

# Redondeo de números
print("Redondeo sin decimales:", round(numero1))
print("Redondeo con 1 decimal:", round(numero1, 1))
print("Redondeo con 2 decimales:", round(numero1, 2))

# Conversión entre tipos
print("\nConversión de tipos:")
print("Convertir flotante a entero:", int(numero1))
print("Convertir entero a flotante:", float(5))