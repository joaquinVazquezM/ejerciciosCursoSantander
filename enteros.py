# Definir dos números enteros
numero1 = 15
numero2 = 7

# Mostrar los números
print("Números enteros:")
print("Número 1:", numero1)
print("Número 2:", numero2)

# Realizar operaciones
print("\nOperaciones:")
print("Suma de número 1 y número 2:", numero1 + numero2)
print("Resta de número 1 y número 2:", numero1 - numero2)
print("Multiplicación de número 1 y número 2:", numero1 * numero2)
print("División entera de número 1 por número 2:", numero1 // numero2)
print("Módulo (resto) de número 1 dividido número 2:", numero1 % numero2)